# Definición de una clase llamada "empleado"
class empleado:
    # Constructor de la clase empleado que recibe cuatro parámetros: código, nombre, telefono y correo.
    def __init__(self, codigo, nombre, telefono, correo):
        # Inicialización del atributo "codigo" con el valor pasado como argumento.
        self.codigo = codigo
        # Inicialización del atributo "nombre" con el valor pasado como argumento.
        self.nombre = nombre
        # Inicialización del atributo "telefono" con el valor pasado como argumento.
        self.telefono = telefono
        # Inicialización del atributo "correo" con el valor pasado como argumento.
        self.correo = correo
