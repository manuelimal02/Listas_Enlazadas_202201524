# Definición de una clase llamada "persona" para representar objetos que contienen información sobre una persona.
class persona:
    # Constructor de la clase "persona" que recibe tres parámetros: nombre, apellido y edad.
    def __init__(self, nombre, apellido, edad):
        # Inicialización del atributo "nombre" con el valor pasado como argumento.
        self.nombre = nombre
        # Inicialización del atributo "apellido" con el valor pasado como argumento.
        self.apellido = apellido
        # Inicialización del atributo "edad" con el valor pasado como argumento.
        self.edad = edad
